    private void calcular_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcular_btnActionPerformed
        int a, b, c; 
        double x1, x2, delta;
        delta = 0;
        
        a = Integer.parseInt(texto01_txt.getText());
        b = Integer.parseInt(texto02_txt.getText());
        c = Integer.parseInt(texto03_txt.getText());
        
        delta = (b * b) + (-4 *(a*c));
        x1 = (Double) ((-(b) + Math.sqrt(delta)) / (2*a));
        x2 = (Double) (((-1 * b) - Math.sqrt(delta)) /(2*a));  
        
        res1_txt.setVisible(true);
        res02_txt.setVisible(true);
        delta_lbl.setVisible(true);
        
        delta_lbl.setText("O delta Ã©: " + delta);
        res1_txt.setText("" + x1);
        res02_txt.setText("" + x2);
    }//GEN-LAST:event_calcular_btnActionPerformed

